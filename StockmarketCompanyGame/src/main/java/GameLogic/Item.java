package GameLogic;

public abstract class Item {
	protected double x;
	protected double y;
	protected double width;
	protected double height;
	
	public Item(double x, double y, double width, double height) {
		super();
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}

	@Override
	public String toString() {
		return "Item [x=" + x + ", y=" + y + ", width=" + width + ", height=" + height + "]";
	}

	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}

	public double getWidth() {
		return width;
	}

	public double getHeight() {
		return height;
	}

	public void setX(double x) {
		this.x = x;
	}

	public void setY(double y) {
		this.y = y;
	}
}